const generateWaferDefectData = () => {
  const data = [];
  for (let i = 0; i < 1000000; i++) {

    let xValue;
    if (Math.random() < 0.7) {

      xValue = (Math.random() * 7) + 8; // Generates values between 8 and 15
    } else {

      xValue = (Math.random() * 2) + 15; // Generates values between 15 and 17
    }

    // Simulate yValue as defect density or measurement value with some variability
    const yValue = Math.floor(Math.random() * 200) + 50; // Defect density from 50 to 250

    data.push([parseFloat(xValue.toFixed(1)), yValue]);
  }
  return data;
};

